    public static wizardFragment newInstance(String param1, String param2) {
        wizardFragment fragment = new wizardFragment();
        Bundle args = new Bundle();
        args.putString(ARG_PARAM1, param1);
        args.putString(ARG_PARAM2, param2);
        fragment.setArguments(args);
        return fragment;
    }

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        if (getArguments() != null) {
            mParam1 = getArguments().getString(ARG_PARAM1);
            mParam2 = getArguments().getString(ARG_PARAM2);
        }
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        View view = inflater.inflate(R.layout.fragment_wizard, container, false);


        ImageView hobbit = (ImageView) view.findViewById(R.id.hobbitImg);

        fm = getActivity().getSupportFragmentManager();

        //when the hobbit image is clicked
        //start an animation 
        //and change the current frame to the hobbit frame
        hobbit.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                FragmentTransaction ft = fm.beginTransaction();
                ft.setCustomAnimations(R.anim.enter_from_right, R.anim.exit_to_left, R.anim.enter_from_left, R.anim.exit_to_right);
                ft.replace(R.id.mainFrame, new hobbitFragment());
                ft.addToBackStack("");
                ft.commit();
            }
        });

        return view;

    }

    // TODO: Rename method, update argument and hook method into UI event
